class Library:
    def  __init__(self):
          self.books=[]

def add_book(book_name,author,publication):
        self.book_name = book_name
        self.author = author
        self.publication = publication

def show_books(self):
    if not self.books:
          print("no book in the library")
          return
    for book in self.books:
            print(f"Name: {book['book_name']}, Author: {book['author']}, Publication: {book['publication']}")

